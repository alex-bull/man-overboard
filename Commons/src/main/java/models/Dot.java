package models;

/**
 * Created by msl47 on 11/05/17.
 * A dot for the boat location
 */
public class Dot {

    private double x;
    private double y;

    public Dot(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
